def find_max(numbers):

    if not numbers:
        return None
    
    max_num = float('-inf')

    for i in range(len(numbers)):
        if numbers[i] > max_num:
            max_num = numbers[i]

    return max_num


numbers = [-1,-2,-3,-4]

